        <h3>Sejarah Desa</h3>
        <p>
            Desa Kumpulrejo adalah desa kolonisasi yang datang dari beberapa daerah di Jawa Tengah dan Jawa Timur,
            Wilayah Timur ditandai dengan adaya tokoh sejarah dari jawa Tengah yaitu Ky Dami yang merupakan kaki tangan dari Temenggung Mertowijojo.
            Wilayah Timur merupakan kawasan pertanian, pada tahun 1857 penduduk desa dengan jumlah yang kurang dari 50 kk.
            Mata pencaharian penduduk pada saat itu hanya mengandalkan hasil pertanian saja.
            mereka membuka dan menggarap tanah yang telah telah ada pada saat itu,
            sedangkan pada wilayah sebelah utara yang ditandai adanya Tokoh Sejarah Demang Aliman yang yang berasal dari
            jawa Timur Beliau sangat terkenal dengan kekuatan dan kadikdayaanya dan memiliki tombak yang dapat berubah menjadi ular besar
            jenis welang. Penduduk sebelah utara bermata pencaharian berkebun di sekeliling rumah dengan ditanamami tanaman polowijo diantaranya
            tanamam koro dan sebagian melaut untuk mencari ikan.
        </p>
        <p>
            Kata Kumpulrejo merupakan do’a yang berasal dari kata kumpul dan redjo.
            Kumpul mempunyai arti bergabung dalam kesatuan masyarakat dalam wilayah Dukuh
            ( Marong wetan, Marong Kulon, Korowelang Lor dan Korowelang Kidul ) sedangkan kata Redjo mempunyai arti keberkahan hasil.
            Jadi Kumpulrejo berarti bergabungnya masyarakat dalam kesatuan wilayah Dukuh untuk kemakmuran dan Keberkahan.
        </p>
        <p>Adapun jabatan kepala desa ataupun lurah Desa Kumpulrejo dari masa kemasa adalah sbb :</p>
        <ol>
            <li>Tahun 1897 – 1921 Demang Aliman</li>
            <li>Tahun 1921 – 1930 Lurah Surat</li>
            <li>Tahun 1930 – 1940 Lurah Simoh</li>
            <li>Tahun 1940 – 1947 Lurah Sumorejo dengan carik Supardi</li>
            <li>Tahun 1947 - 1948 Lurah Supardi</li>
            <li>Tahun 1948 – 1982 Lurah S Pani Carek Slamet Soekadie</li>
            <li>Tahun 1982 - 1985 Pj Carek Slamet Soekadie</li>
            <li>Tahun 1985 – 1993 Lurah Sri Purwati dengan carik Slamet Soekadie</li>
            <li>Tahun 1993 - 2003 Kepala desa M Aksin dengan carik Slamet Soekadie Tahun 2001 Carek Moh Ndhori</li>
            <li>Tahun 2003 - 2008 Kepala desa Trismono dan Moh Ndhori sebagai sekdes</li>
            <li>Tahun 2008 - 2015 Kepala desa Basuki dan Moh Ndhori sebagai sekdes</li>
            <li>Tahun 2015 - 2016 Kepala desa Moh Ndhori (Pj, Kepala Desa) dan Moh Ndhori sebagai sekdes</li>
            <li>Tahun (2016- sekarang ) Basuki dan Moh Ndhori sebagai sekdes (2017) dan Afif Muhammad sebagai Sekdes (2018 – sekarang)</li>
        </ol>
        <p>Demikian sejarah desa Kumpulrejo yang dapat ditulis secara ringkas dan semoga dapat menjadi pengingat warga masyarakat desa kumpulrejo.</p>

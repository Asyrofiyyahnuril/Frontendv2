                        ( Bersih indah barokah aman dan tertib ), mampu meningkatkan pelayanan dan kesejahteraan lahir batin berdasarkan
                        Pancasila dan UUD 1945.
                    </p>
                    <p>
                        Rumusan Visi tersebut merupakan suatu ungkapan dari suatu
                        niat yang luhur untuk memperbaiki dalam Penyelenggaraan Pemerintahan dan
                        Pelaksanaan Pembangunan di Desa Kumpulrejo baik secara individu maupun kelembagaan
